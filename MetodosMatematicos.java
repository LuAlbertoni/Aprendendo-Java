public class MetodosMatematicos {
    public static void main(String[] args) {
        System.out.println(Math.pow(2, 3)); // Exibe o resultado da potência de 2 elevado a 3

        System.out.println(Math.sqrt(9)); // Exibe a raiz quadrada de 9

        System.out.println(Math.cbrt(27)); // Exibe a raiz cúbica de 27

        System.out.println(Math.abs(-10)); // Exibe o valor absoluto de -10

        System.out.println(Math.ceil(10.1)); // Exibe o valor arredondado para cima de 10.1

        System.out.println(Math.floor(10.9)); // Exibe o valor arredondado para baixo de 10.9

        System.out.println(Math.round(10.5)); // Exibe o valor arredondado de 10.5

        System.out.println(Math.max(10, 20)); // Exibe o maior valor entre 10 e 20

        System.out.println(Math.min(10, 20)); // Exibe o menor valor entre 10 e 20
        
        System.out.println(Math.random()); // Exibe um número aleatório entre 0 e 1
    }
}
