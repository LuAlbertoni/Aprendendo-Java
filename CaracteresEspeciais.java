public class CaracteresEspeciais {
    public static void main(String[] args) {
        System.out.println("Teste de aspas duplas \""); // Imprime aspas duplas
        System.out.println("Teste de aspas simples \'"); // Imprime aspas simples
        System.out.println("Teste de barra invertida \\"); // Imprime barra invertida

        System.out.println("Teste de quebra de linha \nTestado"); // Imprime quebra de linha
    }
}
